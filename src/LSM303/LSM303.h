// LSM303 device addresses
#define LSM303_ACCEL_READ_ADDRESS	0x31
#define LSM303_ACCEL_WRITE_ADDRESS	0x30
#define LSM303_MAG_READ_ADDRESS		0x3D
#define LSM303_MAG_WRITE_ADDRESS	0x3C

